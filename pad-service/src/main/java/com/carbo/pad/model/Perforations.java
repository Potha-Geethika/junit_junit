package com.carbo.pad.model;

public class Perforations {

    private double topMD;
    private double bottomMD;
    private double topTVD;
    private double bottomTVD;
    private Integer perfCount;
    private Float size;
    private Float density;

    public double getTopMD() {return topMD;}

    public void setTopMD(double topMD) {this.topMD = topMD;}

    public double getBottomMD() {return bottomMD;}

    public void setBottomMD(double bottomMD) {this.bottomMD = bottomMD;}

    public double getTopTVD() {return topTVD;}

    public void setTopTVD(double topTVD) {this.topTVD = topTVD;}

    public double getBottomTVD() {return bottomTVD;}

    public void setBottomTVD(double bottomTVD) {this.bottomTVD = bottomTVD;}

    public Integer getPerfCount() {return perfCount;}

    public void setPerfCount(Integer perfCount) {this.perfCount = perfCount;}

    public Float getSize() {return size;}

    public void setSize(Float size) {this.size = size;}

    public Float getDensity() {return density;}

    public void setDensity(Float density) {this.density = density;}
}
